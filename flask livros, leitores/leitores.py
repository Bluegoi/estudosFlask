class Leitores():
    def __init__(self):
        self.id = 0
        self.nome = ""
        self.idade = 0
        self.foto = ""
        
    def get_id(self):
        return self.id
    def set_id(self, id):
        self.id = id
    def get_nome(self):
        return self.nome
    def set_nome(self, nome):
        self.nome = nome
    def get_idade(self):
        return self.idade
    def set_idade(self, idade):
        self.idade = idade
    def get_foto(self):
        return self.foto
    def set_foto(self, foto):
        self.foto = foto
            