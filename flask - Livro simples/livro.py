class Livro():
    def __init__(self):
        self.id = 0
        self.nome = ""
        self.autor = ""
        
    def get_id(self):
        return self.id
    def set_id(self, id):
        self.id = id
    def get_nome(self):
        return self.nome
    def set_nome(self, nome):
        self.nome = nome
    def get_autor(self):
        return self.autor
    def set_autor(self, autor):
        self.autor = autor
        
        